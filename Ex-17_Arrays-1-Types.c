#include <stdio.h>
#include <stdlib.h>
//Arrays-Collection of similar kind of data
int main(){
    int myNums[5]={10,20,30,40,50};
    char myChars[5]={'A','B','C','D'};
    float myFloats[5]={1.3,4.5,5.444,3.442,89.43};

    printf("1st int value is %d\n",myNums[0]);
    printf("2nd char value is %c\n",myChars[1]);
    printf("4th float value is %f\n",myFloats[3]);
    return 0;
}

