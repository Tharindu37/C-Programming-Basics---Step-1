#include <stdio.h>
#include <stdlib.h>
//If else -Decision control Structure
int main(){
    int a=10;
    int b=20;

    //if else
    if(a==b){
        printf("A is equal to B\n");
    }else{
        printf("A is not equal to B\n");
    }

    //Multiple Ifs
    if(b==20){
        printf("B is equal to 20\n");
    }
    if(a==10){
        printf("A is equal to 10\n");
    }else{
        printf("A is not equal to 10\n");
    }
    return 0;
}

